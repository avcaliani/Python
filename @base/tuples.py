tuple_one = ("Anthony", "Raquel", "Joe")
tuple_two = (12, 13)

print(tuple_one)                # ('Anthony', 'Raquel', 'Joe')
print(tuple_one[0])             # Anthony
print(tuple_one[0:2])           # ('Anthony', 'Raquel')
print(tuple_one + tuple_two)    # ('Anthony', 'Raquel', 'Joe', 12, 13)